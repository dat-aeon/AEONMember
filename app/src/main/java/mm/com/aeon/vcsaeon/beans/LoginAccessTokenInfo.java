package mm.com.aeon.vcsaeon.beans;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;
import java.io.Serializable;

public class LoginAccessTokenInfo implements Serializable {

    @SerializedName("access_token")
    @Expose
    private String accessToken;

    @SerializedName("token_type")
    @Expose
    private String tokenType;

    @SerializedName("refresh_token")
    @Expose
    private String refreshToken;

    @SerializedName("expires_in")
    @Expose
    private int expiresIn;

    @SerializedName("scope")
    @Expose
    private String scope;

    @SerializedName("jti")
    @Expose
    private String jti;

    @SerializedName("status")
    @Expose
    private String statusCode;

    @SerializedName("userInformationResDto")
    @Expose
    CurrentUserInformationResBean userInformationResDto;

    public String getAccessToken() {
        return accessToken;
    }

    public void setAccessToken(String accessToken) {
        this.accessToken = accessToken;
    }

    public String getTokenType() {
        return tokenType;
    }

    public void setTokenType(String tokenType) {
        this.tokenType = tokenType;
    }

    public String getRefreshToken() {
        return refreshToken;
    }

    public void setRefreshToken(String refreshToken) {
        this.refreshToken = refreshToken;
    }

    public int getExpiresIn() {
        return expiresIn;
    }

    public void setExpiresIn(int expiresIn) {
        this.expiresIn = expiresIn;
    }

    public String getScope() {
        return scope;
    }

    public void setScope(String scope) {
        this.scope = scope;
    }

    public String getJti() {
        return jti;
    }

    public void setJti(String jti) {
        this.jti = jti;
    }

    public String getStatusCode() {
        return statusCode;
    }

    public void setStatusCode(String statusCode) {
        this.statusCode = statusCode;
    }

    public CurrentUserInformationResBean getUserInformationResDto() {
        return userInformationResDto;
    }

    public void setUserInformationResDto(CurrentUserInformationResBean userInformationResDto) {
        this.userInformationResDto = userInformationResDto;
    }
}
