package mm.com.aeon.vcsaeon.beans;

import java.io.Serializable;

public class MessageInfoBean implements Serializable {

    private boolean isButton;
    private boolean isPhoto;
    private String message;
    private String sender;
    private boolean isReceiveMesg;
    private String sendTime;
    private boolean readFlag;
    private int msgId;
    private boolean isIntro;

    public MessageInfoBean(){}

    public MessageInfoBean(boolean isPhoto, String message, String sender, boolean isReceiveMesg,String sendTime,int msgId) {
        this.isPhoto = isPhoto;
        this.message = message;
        this.sender = sender;
        this.isReceiveMesg = isReceiveMesg;
        this.sendTime = sendTime;
        this.msgId = msgId;
    }

    public boolean isButton() {
        return isButton;
    }

    public void setButton(boolean button) {
        isButton = button;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getSender() {
        return sender;
    }

    public void setSender(String sender) {
        this.sender = sender;
    }

    public boolean isReceiveMesg() {
        return isReceiveMesg;
    }

    public void setReceiveMesg(boolean receiveMesg) {
        isReceiveMesg = receiveMesg;
    }

    public String getSendTime() {
        return sendTime;
    }

    public void setSendTime(String sendTime) {
        this.sendTime = sendTime;
    }

    public boolean isReadFlag() {
        return readFlag;
    }

    public void setReadFlag(boolean readFlag) {
        this.readFlag = readFlag;
    }

    public boolean isPhoto() {
        return isPhoto;
    }

    public void setPhoto(boolean photo) {
        isPhoto = photo;
    }

    public int getMsgId() {
        return msgId;
    }

    public void setMsgId(int msgId) {
        this.msgId = msgId;
    }

    public boolean isIntro() {
        return isIntro;
    }

    public void setIntro(boolean intro) {
        isIntro = intro;
    }
}
